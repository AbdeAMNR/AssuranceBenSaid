    public final void loadTable() {
        try {
            //--------------------------------------------------------------------------------------
            String query = "SELECT c.cin, c.nomComplet, u.libaleUsage, q.dateQuitt, q.primeReele, q.reglement, q.perte, cr.creditSum, cr.Diviser, a.typeAttest "
                    + "FROM client c JOIN vehicle v ON c.cin = v.cin JOIN usages u ON v.idUsage = u.idUsage "
                    + "JOIN quittance q ON c.cin = q.cin JOIN fractionnement f ON q.numQuitt = f.numQuitt "
                    + "JOIN attestation a ON f.idAttest = a.idAttest JOIN credit cr ON q.numQuitt = cr.numQuitt ORDER BY q.dateQuitt LIMIT 20;";
            //--------------------------------------------------------------------------------------
            rs = cnx.select(query);
            homeModel.setRowCount(0);
            while (rs.next()) {
                homeModel.addRow(new Object[]{
                    rs.getString("cin"), rs.getString("nomComplet"),
                    rs.getString("libaleUsage"), rs.getDate("dateQuitt"),
                    rs.getFloat("primeReele"), rs.getFloat("reglement"),
                    rs.getFloat("perte"), rs.getFloat("creditSum"),
                    rs.getInt("Diviser"), rs.getString("typeAttest")
                });
            }
            SecurityFunc.resizeColumnWidth(tblPricipale);
        } catch (SQLException e) {
            System.err.println(e);
        }
    }

