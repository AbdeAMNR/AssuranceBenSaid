    private void linkOptRefrechMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_linkOptRefrechMouseClicked
        Timer blinkTimer = new Timer(500, new ActionListener() {
            private int count = 0;
            private int maxCount = 1;
            private boolean on = false;

            @Override
            public void actionPerformed(ActionEvent e) {
                if (count >= maxCount) {
                    pnlOpt.setBackground(Color.decode("#93268F"));
                    ((Timer) e.getSource()).stop();
                } else {
                    pnlOpt.setBackground(Color.decode("#27AE60"));
                    on = !on;
                    count++;
                }
            }
        });
        blinkTimer.start();
        this.loadOpt();
    }//GEN-LAST:event_linkOptRefrechMouseClicked

